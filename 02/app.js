
/* rozwiązanie z pętlą for */
console.log('rozwiązanie z pętlą for');
const x = 4;

for(let i=1; i<=9; i++ ) {
    const result = x * i;
    console.log(`${x} x ${i} = ${result}`)
}


/* rozwiązanie z pętlą while  */
console.log('rozwiązanie z pętlą while');
let y = 1;

while(y <= 10) {
    console.log(`${x} x ${y} = ${x * y}`);
    y++;
}